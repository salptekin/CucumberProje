package day01variables;

public class Variables01 {
	
	public static void main(String[] args) {
		
		//Java'da cumle(Statement) bitince ";" konulur.
		
		//Kod yazarken kirmizi alt cizgi gorurseniz, bu bir hata yaptiginiz anlamina gelir.
		//Bu hatayi duzeltmeden baska kod yazmaya calismayin.
		//Kirmizi alt cizgiye "Compile Time Error" denir.
		
		System.out.println("Suleyman Alp");
		//Ekrana alt alta Ali01, Ali02, Ali03 yazdirin
		System.out.println("Ali01");
		System.out.println("Ali02");
		System.out.println("Ali03");
		
		//Ekrana ayni satira Veli01, Veli02, Veli03 yazdirin
		//Java'da space(bosluk) bir character olarak kabul edilir.
		System.out.print("Veli01 ");//Burada 7 character var ==> V e l i 0 1 ve space
		System.out.print("Veli02 ");
		System.out.println("Veli03");
		
		/* 
		 Ekrana    A
		          A A
		         A A A    yazdiriniz 
		 */
		
		System.out.println("  A  ");
		System.out.println(" A A ");
		System.out.println("A A A");
	
	}

}
